    private void JBTNaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTNaceptarActionPerformed
        // TODO add your handling code here:
		if(!TFJR.getText().equals("")){
			ArrayList<Integer> x1=new ArrayList(),x2=new ArrayList(),x3=new ArrayList();
			int aux = Integer.parseInt(this.TFJR1Inicio.getText());
			int aux2 = Integer.parseInt(this.TFJR2Inicio.getText());
			int aux3 = Integer.parseInt(this.TFJR3Inicio.getText());
			for(int i=0,a=aux,v=aux2,c=aux3;i<Integer.parseInt(this.TFJR.getText());i++,a+=aux,v+=aux2,c+=aux3){
				x1.add(a);
				x2.add(v);
				x3.add(c);
			}

			
			l1 = new Lamport("1",aux,b,fg);
			l2 = new Lamport("2",aux2,b,fg);
			l3 = new Lamport("3",aux3,b,fg);
			
			l1.setTiempos(x1, x2, x3);
			l2.setTiempos(x1, x2, x3);
			l3.setTiempos(x1, x2, x3);

			this.jl1.setText(l1.formatoLista1());
			this.jl2.setText(l2.formatoLista2());
			this.jl3.setText(l3.formatoLista3());
		}
		reloj();
    }//GEN-LAST:event_JBTNaceptarActionPerformed

