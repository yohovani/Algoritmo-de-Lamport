	public void reloj(){
		Runnable r = new Runnable() {
			ArrayList<Integer> a1,a2,a3;
			@Override
			public void run() {
				a1 = l1.getTiempos1();
				a2 = l2.getTiempos2();
				a3 = l3.getTiempos3();
				while(true){
					try {
						for(int i=0;i<a1.size();i++){
							a1.set(i, a1.get(i)+1);
							a2.set(i, a2.get(i)+1);
							a3.set(i, a3.get(i)+1);
						}
						l1.setTiempos1(a1);
						l2.setTiempos2(a2);
						l3.setTiempos3(a3);
						Thread.sleep(1000);
						jl1.setText("");
						jl2.setText("");
						jl3.setText("");
						jl1.setText(l1.formatoLista1());
						jl2.setText(l2.formatoLista2());
						jl3.setText(l3.formatoLista3());
					} catch (InterruptedException ex) {
						Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
					}
				}
			}
		};
		t = new Thread(r);
		
		t.start();
	}
